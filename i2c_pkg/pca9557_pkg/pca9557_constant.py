# Address:

I2CBUS             = 1         # /dev/i2c-1
PCA9557_ADDRESS    = 0x18      # 8 bit version


# Operation Pressure Range

REGISTER0			= 0x00	# Input Port
REGISTER1			= 0x01	# Output Port
REGISTER2			= 0x02	# Inversion Port  I -> Inversion, N -> Normal
REGISTER3			= 0x03	# Setting I/O Port I-> Input, O-> Output
